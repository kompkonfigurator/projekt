<p>Note per unione attributi.</p>
<p>Unire gli attributi significa che i valori degli utenti rimarranno gli stessi, ma che 
gli attuali attributi a cui appartengono vengono uniti in un attributo unico. L\'attributo risultante
&egrave; il primo dei due (secondo l\'ordinamento della lista visibile nella pagina).</p>
<ul>
<li>Puoi unire soltanto attributi dello stesso tipo</li>
<li>Quando li unisci, il valore del primo attributo viene mantenuto (se esistente), oppure
sovrascritto dal valore dell'attributo che gli viene unito. Questo pu&ograve; causare perdita
di dati, nel caso in cui entrambi gli attributi hanno un valore. </li>
<li>Se unisci attributi del tipo <i>checkbox</i> l'attributo risultante dall'unione diventa di tipo <i>checkboxgroup</i>.</li>
<li>Gli attributi correttamente uniti ad un altro vengono cancellati dopo l'unione.</li>
</ul>
