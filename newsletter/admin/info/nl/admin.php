<p>Plaats rechten om pagina\'s in het systeem te lezen:</p>
<ul>
<li>Alle: admin heeft toegang tot de pagina zonde beperkingen</li>
<li>Kijken: admin kan de inhoud van de pagina\'s zien, maar kan niks veranderen. Dit werkt momenteel enkel
voor de "gebruiker", "gebruikers" en "leden" pagina\'s</li>
<li>Geen: admin kan deze pagina niet zien</li>
<li>Eigenaar: admin kan deze pagina zien, maar ziet enkel de inhoud van de lijsten waarvan hij eigenaar is</li>
</ul>
