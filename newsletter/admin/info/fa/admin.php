<p>تنظیم مجوزها برای دیدن برگه ها در سیستم:</p>
<ul>
<li>همه: مدیر بدون محدودیت به برگه دسترسی دارد</li>
<li>دید: مدیر میتواند محتوای برگه را ببیند ولی نمیتواند چیزی را تغییر دهید. فعلاً فقط برای برگه های "کاربر"، "کاربرها" و "اعضا" کار می کند.</li>
<li>هیچ: مدیر نمیتواند این برگه را ببیند</li>
<li>صاحب: مدیر میتواند این برگه را ببیند، اما فقط محتوای فهرستهای مال خودش است</li>
</ul>
