<ul>
<li>Todas: admin tem acesso &agrave;s p&aacute;ginas sem nenhuma restri&ccedil;&atilde;o</li>
<li>Visualizar: admin pode ver o conte&uacute;do das p&aacute;ginas, mas sem poder modific&aacute;-las. Atualmente s&oacute; funciona para p&aacute;ginas de "usu&aacute;rio", "usu&aacute;rios" e "membros'.</li>
<li>Nenhuma: admin n&atilde;o pode visualizar esta p&aacute;gina</li>
<li>Administrativa: admin pode visualizar esta p&aacute;gina, mas somente o conte&uacute;do das listas que eles administram</li>
</ul>
